func binarySearch(_ array: [Int], _ value: Int) -> Int? {
  var l = 0
  var r = array.count - 1

  if l > r { return nil }

  while l <= r {
    var m: Int = (l+r) / 2
    if array[m] == value { 
      return m 
    } else if array[m] > value {
      r = m - 1
    } else {
      l = m + 1
    }
  }

  return nil

  // this was incorrect?
  if l == value {
    return l
  } else {
    return nil
  }
}

